const section = document.querySelector('.profiles-grid');


robots.forEach(robot => {
    const robotCell = document.createElement('div');
    robotCell.classList.add('profile-card');
    
    const robotPic = document.createElement('img');
    robotPic.src = robot.image
    robotPic.classList.add('profile-pic')
    robotName = document.createElement('h2')
    robotName.appendChild(document.createTextNode(robot.name))
    robotEmail = document.createElement('p')
    robotEmail.appendChild(document.createTextNode(robot.email))

    robotCell.appendChild(robotPic)
    robotCell.appendChild(robotName)
    robotCell.appendChild(robotEmail)

    section.appendChild(robotCell);
});

function searchFunction() {
    let filter = document.getElementById('search').value.toUpperCase();
    
    let cards = document.getElementsByClassName('profile-card');
    
    for (let i = 0; i < cards.length; i++) {
        let name = cards[i].getElementsByTagName('h2')[0];
        if (name) {
            let txtValue = name.textContent || name.innerText;
            if (txtValue.toUpperCase().indexOf(filter) > -1) {
                cards[i].style.display = "";
            } else {
                cards[i].style.display = "none";
            }
        }
    }
}

